class GalleryLinksRewriter implements MarkupRewriter {

/*
  <textarea name="message" id="vB_Editor_001_textarea" rows="10" cols="60" style="display:block; width:540px; height:250px" tabindex="1" dir="ltr"></textarea>
*/

  public function process($content) {
    return preg_replace(
      '@href\s*=\s*"/gallery/images/@',
      'href="http://www.amiga.org/gallery/images/',
      $content
    );
  }

}
